async function seedTranslations() {
  console.log("Starting translation seeding...");

  // Get all languages
  const allLanguages = await db.select().from(languages);
  console.log(`Found ${allLanguages.length} languages`);

  // Get all translation keys
  const allKeys = await db.select().from(translationKeys);
  console.log(`Found ${allKeys.length} translation keys`);

  let insertedCount = 0;
  let skippedCount = 0;
  let notFoundCount = 0;

  for (const [langCode, namespaces] of Object.entries(translations)) {
    const language = allLanguages.find(l => l.code === langCode);
    if (!language) {
      console.log(`Language ${langCode} not found in database, skipping...`);
      continue;
    }

    console.log(`\nProcessing ${language.name} (${langCode})...`);

    for (const [namespace, keys] of Object.entries(namespaces)) {
      for (const [keyName, value] of Object.entries(keys)) {
        // Find the translation key
        const key = allKeys.find(k => k.namespace === namespace && k.key === keyName);
        
        if (!key) {
          // Key doesn't exist in database
          notFoundCount++;
          continue;
        }

        // Check if translation already exists
        const existing = await db
          .select()
          .from(translationValues)
          .where(
            and(
              eq(translationValues.keyId, key.id),
              eq(translationValues.languageId, language.id)
            )
          );

        if (existing.length > 0) {
          skippedCount++;
          continue;
        }

        // Insert the translation
        await db.insert(translationValues).values({
          keyId: key.id,
          languageId: language.id,
          value: value,
          isVerified: true,
        });

        insertedCount++;
      }
    }

    console.log(`  Completed ${langCode}`);
  }

  console.log(`\n=== Translation Seeding Complete ===`);
  console.log(`Inserted: ${insertedCount} translations`);
  console.log(`Skipped (already exists): ${skippedCount} translations`);
  console.log(`Keys not found in DB: ${notFoundCount}`);
}

seedTranslations()
  .then(() => {
    console.log("Done!");
    process.exit(0);
  })
  .catch((err) => {
    console.error("Error seeding translations:", err);
    process.exit(1);
  });
